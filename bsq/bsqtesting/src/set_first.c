int **set_first(int row, int col, short **m, int **s)
{
	int i;
	int j;

	i = 0;
	while (i < row)
	{
		s[i][0] = m[i][0];
		i++;
	}
	j = 0;
	while (j < col)
	{
		s[0][j] = m[0][j];
		j++;
	}
	return (s);
}
